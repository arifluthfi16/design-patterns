public class Barang {
    public String nama;
    public int jumlah;
    public int beratSatuan;
    public String jenis;

    public Barang(String nama, int jumlah, int beratSatuan, String jenis) {
        this.nama = nama;
        this.jumlah = jumlah;
        this.beratSatuan = beratSatuan;
        this.jenis = jenis;
    }
}
